<template>
    <div class="w3-section">
        <input class="w3-input w3-show-inline-block eighty" type="text"
            v-model="value"
            v-on:keyup="validate()">
        <Check v-if="isValid"></Check>
        <Cross v-else></Cross>
        <label class="w3-show-block">{{ label }}</label>
    </div>
</template>

<script>
    import Check from './Check';
    import Cross from './Cross';

    export default {
        props: {
            label: {
                type: String
            },
            validation: {
                type: String
            }
        },
        data() {
            return {
                value: '',
                isValid: false
            }
        },
        methods: {
            validate() {
                this.$emit('setValue', this.value);
                switch(this.validation) {
                    case 'ZIP':
                        if(/(^\d{5}$)|(^\d{5}-\d{4}$)/.test(this.value)) {
                            this.isValid = true;
                        } else {
                            this.isValid = false;
                        }
                        break;
                    default:
                        if(this.value != '') {
                            this.isValid = true;
                        } else {
                            this.isValid = false;
                        }
                        break;
                }
            }
        },
        components: {
            Check,
            Cross
        }
    }
</script>
